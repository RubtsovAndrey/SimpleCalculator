namespace SimpleCalculator.Models;

// ============================================================================
// МОДЕЛЬ ДАННЫХ: Запрос на вычисление
// ============================================================================

// Эта модель описывает структуру данных, которые приходят от клиента
// Она используется для Model Binding - автоматического преобразования
// данных из HTTP-запроса (JSON, форма, query-параметры) в C# объект

/// <summary>
/// Модель запроса на выполнение математической операции
/// </summary>
/// <remarks>
/// Используется в двух сценариях:
/// 1. HTML форма (POST /calc/calculate) - данные приходят как application/x-www-form-urlencoded
/// 2. JSON API (POST /calc/api) - данные приходят как application/json
/// 
/// ASP.NET автоматически парсит данные в этот объект благодаря Model Binding
/// </remarks>
public class CalculationRequest
{
    // ========================================================================
    // СВОЙСТВА (PROPERTIES)
    // ========================================================================
    
    // В C# свойства - это специальный синтаксис для инкапсуляции полей
    // Вместо:
    //   private double number1;
    //   public double GetNumber1() { return number1; }
    //   public void SetNumber1(double value) { number1 = value; }
    // 
    // Мы пишем:
    //   public double Number1 { get; set; }
    //
    // Компилятор автоматически создаёт скрытое поле и методы get/set
    
    /// <summary>
    /// Первое число для вычисления
    /// </summary>
    /// <example>10.5</example>
    public double Number1 { get; set; }
    
    /// <summary>
    /// Второе число для вычисления
    /// </summary>
    /// <example>5.2</example>
    public double Number2 { get; set; }
    
    /// <summary>
    /// Математическая операция: "+", "-", "*", "/"
    /// </summary>
    /// <example>"+"</example>
    /// <remarks>
    /// Значение по умолчанию = "+" (если клиент не передал)
    /// Синтаксис = "+" - это инициализатор свойства
    /// </remarks>
    public string Operation { get; set; } = "+";
    
    // ========================================================================
    // ПОЧЕМУ ИМЕННО ТАК?
    // ========================================================================
    
    // 1. Почему double, а не int?
    //    - double поддерживает дробные числа (10.5, 3.14)
    //    - int только целые (10, 3)
    //    - Для калькулятора нужны дробные результаты деления
    
    // 2. Почему string для операции, а не enum?
    //    - Для простоты (enum требует дополнительной настройки JSON)
    //    - В реальном проекте лучше использовать enum для безопасности типов
    
    // 3. Почему свойства, а не поля?
    //    - Model Binding работает только со свойствами (не с полями)
    //    - Свойства можно валидировать атрибутами ([Required], [Range])
    //    - Свойства - это стандарт в C# для публичных данных
    
    // 4. Почему { get; set; }, а не { get; }?
    //    - Model Binding должен ЗАПИСАТЬ данные в объект (нужен set)
    //    - Только { get; } сделало бы свойство read-only
}

