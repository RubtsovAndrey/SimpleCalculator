namespace SimpleCalculator.Services;

// ============================================================================
// СЕРВИС: Бизнес-логика калькулятора
// ============================================================================

// Сервис - это класс, который содержит бизнес-логику приложения
// Он отделён от контроллера по принципу Separation of Concerns:
// - Контроллер отвечает за HTTP (принять запрос, вернуть ответ)
// - Сервис отвечает за логику (вычисления, работа с данными)

/// <summary>
/// Сервис для выполнения математических операций
/// </summary>
/// <remarks>
/// Зарегистрирован как Singleton в Program.cs
/// Это означает, что создаётся ОДИН экземпляр на всё приложение
/// Все запросы используют один и тот же объект
/// </remarks>
public class CalculatorService
{
    // ========================================================================
    // ПОЛЯ (FIELDS)
    // ========================================================================
    
    // readonly означает: значение можно присвоить только в конструкторе
    // После этого изменить нельзя (immutable)
    // Это гарантирует, что _logger не будет случайно перезаписан
    private readonly ILogger<CalculatorService> _logger;
    
    // Префикс _ (underscore) - это соглашение об именовании для приватных полей
    // Позволяет отличить поле от локальной переменной или параметра:
    // - _logger - поле класса
    // - logger - параметр метода
    
    // ========================================================================
    // КОНСТРУКТОР
    // ========================================================================
    
    // Конструктор вызывается при создании объекта
    // Dependency Injection автоматически передаёт зависимости в параметры
    
    /// <summary>
    /// Конструктор сервиса
    /// </summary>
    /// <param name="logger">Логгер для записи событий (внедряется через DI)</param>
    /// <remarks>
    /// ILogger - это интерфейс из Microsoft.Extensions.Logging
    /// DI-контейнер автоматически создаёт реализацию и передаёт сюда
    /// Мы не создаём logger вручную (new Logger()) - это делает DI
    /// </remarks>
    public CalculatorService(ILogger<CalculatorService> logger)
    {
        // Сохраняем переданный логгер в поле класса
        // Теперь мы можем использовать _logger в любом методе
        _logger = logger;
        
        // ВАЖНО: В C# параметры конструктора обычно называются с маленькой буквы (logger)
        // А свойства/поля - с большой буквы или с _ (Logger или _logger)
    }
    
    // ========================================================================
    // МЕТОДЫ
    // ========================================================================
    
    /// <summary>
    /// Выполняет математическую операцию над двумя числами
    /// </summary>
    /// <param name="num1">Первое число</param>
    /// <param name="num2">Второе число</param>
    /// <param name="operation">Операция: "+", "-", "*", "/"</param>
    /// <returns>Результат вычисления</returns>
    /// <exception cref="DivideByZeroException">Если операция "/" и num2 = 0</exception>
    /// <exception cref="ArgumentException">Если операция неизвестна</exception>
    public double Calculate(double num1, double num2, string operation)
    {
        // ====================================================================
        // ЛОГИРОВАНИЕ
        // ====================================================================
        
        // LogInformation - уровень логирования "Information"
        // Используется для обычных событий (не ошибки, не предупреждения)
        
        // {Num1}, {Operation}, {Num2} - это placeholders для структурированного логирования
        // Значения подставляются из следующих аргументов: num1, operation, num2
        // 
        // Результат в логе:
        // [Information] Вычисление: 10 + 5
        //
        // Преимущества структурированного логирования:
        // - Системы мониторинга (Serilog, Application Insights) сохраняют
        //   Num1, Operation, Num2 как отдельные поля
        // - Можно фильтровать: "покажи все вычисления, где Num1 > 100"
        _logger.LogInformation("Вычисление: {Num1} {Operation} {Num2}", num1, operation, num2);
        
        // ====================================================================
        // SWITCH EXPRESSION (C# 8.0+)
        // ====================================================================
        
        // Современный синтаксис switch, который ВОЗВРАЩАЕТ значение
        // Старый синтаксис (switch statement):
        //   switch (operation)
        //   {
        //       case "+": return num1 + num2;
        //       case "-": return num1 - num2;
        //       ...
        //   }
        //
        // Новый синтаксис (switch expression) - короче и выразительнее:
        return operation switch
        {
            // Паттерн: значение => результат
            "+" => num1 + num2,
            "-" => num1 - num2,
            "*" => num1 * num2,
            
            // Тернарный оператор: условие ? если_true : если_false
            // Проверяем деление на ноль перед выполнением
            "/" => num2 != 0 
                ? num1 / num2 
                : throw new DivideByZeroException("Деление на ноль!"),
            
            // _ (underscore) - это "default case" (все остальные значения)
            // Выбрасываем исключение для неизвестных операций
            _ => throw new ArgumentException($"Неизвестная операция: {operation}")
        };
        
        // ====================================================================
        // ИСКЛЮЧЕНИЯ (EXCEPTIONS)
        // ====================================================================
        
        // throw - ключевое слово для выбрасывания исключения
        // Исключение прерывает нормальный ход выполнения и передаёт управление
        // в ближайший блок catch (или завершает программу, если catch нет)
        //
        // DivideByZeroException - встроенный тип исключения для деления на ноль
        // ArgumentException - встроенный тип для некорректных аргументов
        //
        // Контроллер поймает эти исключения в try-catch и вернёт
        // соответствующий HTTP-ответ (400 Bad Request или 500 Internal Server Error)
    }
    
    // ========================================================================
    // ПОЧЕМУ ИМЕННО ТАК?
    // ========================================================================
    
    // 1. Почему сервис, а не логика в контроллере?
    //    - Разделение ответственности: контроллер = HTTP, сервис = логика
    //    - Тестируемость: сервис можно тестировать без HTTP-запросов
    //    - Переиспользование: логику можно вызвать из разных контроллеров
    
    // 2. Почему Singleton lifetime?
    //    - Сервис не хранит состояние (stateless)
    //    - Все методы работают только с параметрами
    //    - Один экземпляр эффективнее по памяти
    //    - Если бы сервис хранил данные (например, историю вычислений),
    //      нужен был бы Scoped или добавление потокобезопасности (lock)
    
    // 3. Почему ILogger внедряется через конструктор?
    //    - Это паттерн Dependency Injection (Constructor Injection)
    //    - Класс явно объявляет свои зависимости
    //    - DI-контейнер автоматически их предоставляет
    //    - Легко подменить в тестах (mock)
    
    // 4. Почему double, а не decimal?
    //    - double - для научных вычислений (быстрее, но есть погрешность)
    //    - decimal - для финансовых расчётов (точнее, но медленнее)
    //    - Для простого калькулятора double достаточно
}
